#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	return_size;
	size_t	i;

	i = 0;
	return_size = ft_strlen(src);
	if (size == 0)
		return (return_size);
	while (src[i] && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (return_size);
}
